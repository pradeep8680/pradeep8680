package Num;

public class Challenge4 {

	public static void main(String[] args) {
		int	a=9;
		
		a += 1;								
		
		System.out.println(a);
		
		a -= 1;
		
		System.out.println(a);
		
		a *= 1;
		
		System.out.println(a);
		
		a /= 1;
		
		System.out.println(a);
		
		a %= 1;
		
		System.out.println(a);
		
		a ^= 1;
		
		System.out.println(a);
		
		a >>= 1;
		
		System.out.println(a);
		
		a <<= 1;
		
		System.out.println(a);
		
		a &= 1;
		
		System.out.println(a);
		
		a |= 1;
		
		System.out.println(a);
				
	}

}
